// Today We Will Be Learning About Using The forEach() Method,
// With Maps And Sets, This Subrepo Is More Of A Detour, But Its Still Good To Know,
// So, Lettuce Begin🥬!

// I'll Be Taking The Currencies Map From Above, Commenting It Out There,
// And Placing It Right Below,
const currencies = new Map([
  ['USD', 'United States dollar'],
  ['EUR', 'Euro'],
  ['GBP', 'Pound sterling'],
]);

currencies.forEach(function (value, key, map) {
  console.log(`${key}: ${value}`);
});

// Now, Let's Do The Same Thing With A Set,
// First, I'll Create A Set Of Unique Currencies,
const currenciesUnique = new Set(['USD', 'GBP', 'USD', 'EUR', 'EUR']);

// Then, I'll Use The forEach() Method To Log Each Value In The Set,
currenciesUnique.forEach(function (value, _, set) { // <-- An Underscore In JavaScript Is Used For Throwaway Variables, Meaning Variables That Are Completely Unecessary,
  console.log(`${value}: ${value}`);
});
